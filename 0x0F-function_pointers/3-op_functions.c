#include <stdio.h>
#include "3-calc.h"

/**
 * op_add - addition function
 * @a: int a
 * @b: int b
 * Description - this is the description
 * Return: return type
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - function to subtract
 * @a: int a
 * @b: int b
 * Description -  this is the description
 * Return: return type
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - function to multiply
 * @a: int a
 * @b: int b
 * Description - this is the description
 * Return: return type
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - function to divide
 * @a: int a
 * @b: int b
 * Description - this is the description
 * Return: return type
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - function to get remainder
 * @a: int a
 * @b: int b
 * Description - this is the description
 * Return: return type
 */
int op_mod(int a, int b)
{
	return (a % b);
}
